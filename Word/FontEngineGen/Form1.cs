        private void WriteJS(ref StreamWriter oWriter, string sFileName, bool is_config)
        {
            StreamReader oReader = new StreamReader(sFileName);
            string sTemp = oReader.ReadToEnd();

            if (is_config)
            {
                var array_keys = _dict.Keys.ToArray();
                for (int i = 0; i < _dict.Count; i++)
                {
                    string key = array_keys[i];
                    string value = _dict[array_keys[i]];

                    sTemp = sTemp.Replace(key+" ",value+" ");
                    sTemp = sTemp.Replace(key + ";", value + ";");
                    sTemp = sTemp.Replace(key + ":", value + ":");
                    sTemp = sTemp.Replace(key + ")", value + ")");
                    sTemp = sTemp.Replace(key + "=", value + "=");
                    sTemp = sTemp.Replace(key + ",", value + ",");
                }
            }

            oWriter.Write(sTemp);
        }

